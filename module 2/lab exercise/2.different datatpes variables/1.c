#include<stdio.h>
int main()
{
	int age=15;//integer type variable
	float percentage=89.656; //decimal type variable
	char grade='A';//character type variable
	const  int roll_number=101;//Declaring constant using const word
	printf("\nThe age is  %d",age);
	printf("\nThe percentage is %.2f",percentage);
	printf("\nThe grade is %c",grade);
	printf("\nThe roll number is %d",roll_number);
	return 0;
}
